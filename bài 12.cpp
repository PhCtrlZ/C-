#include <iostream>
using namespace std;

int main()
{
	// example
	// cac toan tu la cong tru nhan chia binh thuong con %
	// la chia lay du / chia lay phan nguyen
	cout <<"7+3=" << 7+3 << endl;
	cout <<"7-3=" << 7-3 << endl;
	cout <<"7*3=" << 7*3 << endl;
	cout <<"7/3=" << 7/3 << endl;
	cout <<"7%3=" << 7%3 << endl;
	// tang giam tien to va tang giam hau to
	int x=69;
	int y=++x;
	cout <<x<<endl;
	cout <<y<<endl;
	// hau to
	int z=69;
	int e=z++;
	cout << z<< endl;
	cout << e<< endl;
	// cac toan tu gan += -+ *= /=
	
}